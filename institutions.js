export function findInstitutionById(institutionId) {
  return (
    INSTITUTIONS.find(
      (institution) => institution.id === institutionId
    ) || null
  );
}

export function searchInstitutions(query) {
  if (!query) return [];

  const normalizedQuery = normalizeText(query);

  return INSTITUTIONS.filter((institution) =>
    normalizeText(institution.name).includes(normalizedQuery)
  );
}

export function getInstitutionContext(institutionId) {
  const institution = findInstitutionById(institutionId);

  if (!institution) {
    return `
Institución: No identificada.
Datos de soporte: No disponibles.
`;
  }

  return `
Institución: ${institution.name}
Producto: ${institution.product || "No disponible"}
LMS: ${institution.lms || "No disponible"}
Teléfono de soporte: ${institution.supportPhone || "No disponible"}
Mail de soporte: ${institution.supportEmail || "No disponible"}
`;
}

function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim();
}
